// get random word  
export function randomFrom(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

export function randomName(twoPrefixChance = 0.2) {
   const prefix1 = randomFrom(prefixes);
   const noun = randomFrom(nouns);

   if(Math.random() < twoPrefixChance){
    let prefix2 = randomFrom(prefixes);

    while(prefix2 === prefix1){
        prefix2 = randomFrom(prefixes);
    }

    return `${prefix1} ${prefix2} ${noun}`;
   }

   return `${prefix1} ${noun}`;
}